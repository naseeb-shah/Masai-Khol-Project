function displayData(womensdata) {
    document.querySelector(".items").innerHTML = "";

    womensdata.map(function (ele, i, arr) {
        var div = document.createElement("div");
        div.setAttribute("class", "itm");

        var image = document.createElement("img");
        image.setAttribute("src", ele.img);
         image.addEventListener("click", function () {
             openPage(ele);
         });

        var saleprice = document.createElement("h3");
        saleprice.setAttribute("class", "o_p");
        saleprice.innerText = "$" + ele.saleprice;

        var originalprice = document.createElement("p");
        originalprice.innerText = "$" + ele.originalprice;
        originalprice.setAttribute("class", "r_p");

        var name = document.createElement("h5");
        name.setAttribute("class", "name");
        name.setAttribute("title", ele.name);
        name.addEventListener("click", function () {
            openPage(ele);
        });
        name.innerText = ele.name;

        // var gender = document.createElement()

        div.append(image, saleprice, originalprice, name);
        document.querySelector(".items").append(div);
        document.querySelector(".count").innerText = womensdata.length;
    });
}

